enum ThemeButtonType {
  /// A button with a background color set as the theme's primary color
  primary,

  /// A button with a background color that is a darker shade of the theme's
  secondary,

  /// A button with a background color that is a darker shade of the theme's
  /// primary color
  alternative,

  /// A button with a border color and a background color as the button's
  /// of alternative type
  outline,

  /// A button text like a link
  text,

  /// A button with an icon only
  icon,
}
